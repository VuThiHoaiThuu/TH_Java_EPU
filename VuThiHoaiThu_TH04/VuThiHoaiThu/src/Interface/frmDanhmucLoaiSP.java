    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String ml = txtMaLoai.getText();
        if(ml.length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn", "Thông báo", 1);
        } else {
            this.setKhoa(false);
            txtMaLoai.enable(false);
            this.setButton(false);
            cothem = false;
        }
        
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String ml = txtMaLoai.getText();
        String tl = txtTenLoai.getText();
        
        if(ml.length() == 0 || tl.length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn mã loại và tên loại", "Thông báo", 1);
        } else {
            if(ml.length() > 2 || tl.length() > 20) {
                JOptionPane.showMessageDialog(null, "Mã loại chỉ 2 kí tự tên loại là 20", "Thông báo", 1);               
            } else {
                try {
                    sp.setMaSP(ml);
                    sp.setTenSP(tl);  
                    if(cothem == true) {                 
                        sp.insertData(sp);
                    } else {
                        sp.editData(sp);
                    }
                    
                    this.clearData();
                    this.ShowData();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Thông báo", 1);
                }
                
                this.setNull();
                this.setKhoa(false);
                this.setButton(true);
            }
        }
        
    }//GEN-LAST:event_btnLuuActionPerformed


    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String maLoai = txtMaLoai.getText();
        try {
            if(maLoai.length() == 0) {
                JOptionPane.showMessageDialog(null, "Chọn 1 loại cần xóa", "Thông báo", 1);              
            } else {
                if(JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa" + maLoai + " này không ?", "Thông báo", 2) == 0) {
                    sp.deleteData(maLoai);
                    this.clearData();
                    this.ShowData();
                    this.setNull();                  
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Xóa thất bại !", "Thông báo", 1);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
        // TODO add your handling code here:
        try {
            int row = tbSanPham.getSelectedRow();
            String ml = (tbSanPham.getModel().getValueAt(row, 0).toString());
            LoaiSP obj = sp.getLoaiSP(ml);
            if(obj != null) {
                txtMaLoai.setText(ml);
                txtTenLoai.setText(obj.getTenSP());
            }           
        } catch(Exception e) {
            
        }
    }//GEN-LAST:event_tbSanPhamMouseClicked


    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        String msp = txtMaSP.getText();
        if(msp.length() == 0)
            JOptionPane.showMessageDialog(null, "Vui long chọn loại cần sửa", "Thông báo", 1);
        else{
            try {
                sp.EditData(sp);
            } catch (SQLException ex) {
                Logger.getLogger(frmCapnhatSP.class.getName()).log(Level.SEVERE, null, ex);
            }
            setKhoa(false);
            setButton(false);
            cothem = false;
        }
    }//GEN-LAST:event_btnSuaMouseClicked

    private void btnLuuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLuuMouseClicked
        String msp = txtMaSP.getText();
        String tsp = txtTenSP.getText();
        String DonGia = txtGiaBan.getText();
        long gia = Long.parseLong(DonGia);
        String maloai = txtLoaiSP.getText();
        
        
        if(msp.length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điềm thông tin ", "Thông báo", 1);
        } else {
            if(msp.length() > 5) {
                JOptionPane.showMessageDialog(null, "Mã loại không hợp lệ", "Thông báo", 1);               
            } else {
                try {
                    sp.setMaSP(msp);
                    sp.setTenSP(tsp);
                    sp.setDonGia(gia);
                    sp.setMaLoai(maloai);
                    
                    if(cothem == true) {                 
                        sp.InsertData(sp);
                    } else {
                        sp.EditData(sp);
                    }
                    
                    this.ClearData();
                    this.ShowData();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Thông báo", 1);
                }
                
                this.setNull();
                this.setKhoa(false);
                this.setButton(true);
            }
        }
    }//GEN-LAST:event_btnLuuMouseClicked


    private void tbCapNhatSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCapNhatSPMouseClicked
        // TODO add your handling code here:
        try {
            int row = tbCapNhatSP.getSelectedRow();
            String ma = (tbCapNhatSP.getModel().getValueAt(row, 0)).toString();
            SanPham rs = sp.getSanPhamTheoMa(ma);
            if(rs != null){
                txtMaSP.setText(rs.getMaSP());
                txtTenSP.setText(rs.getTenSP());
                String Gia = String.valueOf(rs.getDonGia());
                
                txtGiaBan.setText(Gia);
                txtLoaiSP.setText(rs.getMaLoai());
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tbCapNhatSPMouseClicked

    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        String msp = txtMaSP.getText();
        try {
            if(msp.length() == 0) {
                JOptionPane.showMessageDialog(null, "Chọn 1 loại cần xóa", "Thông báo", 1);              
            } else {
                if(JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa" + msp + " này không ?", "Thông báo", 2) == 0) {
                    sp.DeleteData(msp);
                    this.ClearData();
                    this.ShowData();
                    this.setNull();                  
                }
            }
        } catch (Exception e) {
            Logger.getLogger(frmCapnhatSP.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnXoaMouseClicked


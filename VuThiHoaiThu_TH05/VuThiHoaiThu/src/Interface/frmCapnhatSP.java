    final void ShowDataCombo() {
        List<LoaiSP> result = null;
        try {
            result = lsp.getLoaiSP();
            for(int i = 0; i < result.size(); i++){
                cboLoaiSP.addItem(result.get(i).getMaLoai());
            }
        } catch (Exception e) {
        }
    }
    
    public void ShowTenloai(String ma) {
        txtLoaiSP.setText(lsp.TenLoaiSPTheoMa(ma));
    }
    
    public void ClearData() throws SQLException {
        int n = tableModel.getRowCount()-1;
        for(int i = n;i >= 0; i--) {
            tableModel.removeRow(i);
        }
         
    }
    
    public final void ShowData() throws SQLException {
        List<SanPham> result = sp.getSanPham();
        try {
            ClearData();
            for(int i = 0; i < result.size(); i++){
                Object[] rows = new Object[4];
                rows[0] = result.get(i).getMaSP();
                rows[1] = result.get(i).getTenSP();
                rows[2] = result.get(i).getDonGia();
                rows[3] = result.get(i).getMaLoai();
                tableModel.addRow(rows);
            }
        } catch (Exception e) {
        }
    }
    
